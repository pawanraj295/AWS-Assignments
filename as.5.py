base = int(input("Enter the base number: "))
exponent = int(input("Enter the exponent: "))

result = base ** exponent

print(f"The result of {base} raised to the power of {exponent} is: {result}") 


# Left Shift Example:
num = 10 
shifted_num = num << 2  
print(f"The result of left shift is: {shifted_num}")

# Right Shift Example:
num = 10    
shifted_num = num >> 2   
print(f"The result of right shift is: {shifted_num}") 

# Bitwise AND Example:

num1 = 5    
num2 = 3     
result = num1 & num2  
print(f"The result of bitwise AND is: {result}")  

# Example of 'and' Operator
a = True
b = False
result = a and b
print(f"The result of 'and' operator is: {result}") 

#Example of '&' Operator:
num1 = 5
num2 = 3
result = num1 & num2
print(f"The result of '&' operator is: {result}")